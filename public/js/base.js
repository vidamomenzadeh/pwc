class FocusGame {
  constructor(){
    this.results = {}
    this.index = 0;

    this.saveResult = this.saveResult.bind(this)

    $( ".btn-submit" ).on( "click", this.showDescription);
    $( ".btn-start" ).on( "click", this.showTarget);
    $('.buttons-wrapper div').on( "click", this.saveResult);
  }

  sendResult = (accuracy, responseTime, resultsTrials) => {
    const userCode = $('#user-code').val() || ''

    $.ajax({
      type: "POST",
      url: '/game/add',
      "Content-Type": "application/json;charset=utf-8",
      data: {accuracy, responseTime, userCode: userCode, resultsTrials: JSON.stringify(resultsTrials)},
      success: function (response) {
        console.log('sdafdsd')
     },
     error: function(jqXHR, textStatus, errorThrown) {
        console.log(textStatus, errorThrown);
     }
    });

    this.showThanks()
  }

  showTarget = () => {
    $('.main-steps').hide()
    $('.main-target').show()
    window.durationtimeout = setTimeout(() => {
     this.run()
    }, 3000);
  }

  showDescription = () => {
    $('.main-steps').hide()
    $('.main-description').show()
  }

  showThanks = () =>{
    $('.main-steps').hide()
    $('.main-Thanks').show()
  }

  goToNextTrial = () => {
    clearTimeout(window.pausetimeout)
    clearTimeout(window.durationtimeout)
    this.results[this.index].endTime = new Date();

    if(this.index < imagesDetails.length - 1){
      this.index ++;
      this.run()
    }else{
      this.calculateScore()
    }
  }

  calculateScore = () => {
      let responseTime = 0
      let accuracy = 0
      let resultsTrials = []
      let currentResponseTime

      for (var i = 0; i < imagesDetails.length; i++) {
        let trialDetail = this.results[i];

        if (trialDetail.correct != undefined) {
            currentResponseTime = trialDetail.endTime ? (trialDetail.endTime.getTime() - trialDetail.startTime.getTime()) : 0;
            responseTime += currentResponseTime;
            accuracy += trialDetail.correct;
        }
        resultsTrials[i] = {
          accuracy: trialDetail.correct,
          responseTime : currentResponseTime,
          index: i
        }
    }

    accuracy = imagesDetails.length > 0 ? parseFloat((accuracy / imagesDetails.length * 100).toFixed(2)) : 0;
    responseTime = imagesDetails.length > 0 ? parseFloat((responseTime / imagesDetails.length).toFixed(2)) : 0;

    this.sendResult(accuracy, responseTime, resultsTrials)
  }

  renderTrial = () => {
    let images = ''

    for(let i = 0; i < imagesDetails[this.index].trials.length; i++){
      const trial = imagesDetails[this.index].trials[i]

      images += `<div class="focus-img-wrapper" style="top:${(trial.y - 1) * 100 / 4}%;left:${(trial.x - 1) * 100 / 6}%">`+
      `<div class="focus-item-image" style="background-image:url('../assets/${trial.name}.png');"></div>` +
      '<div class="border-image-box"></div>' +
      '</div>'
    }

    $('.main-steps').hide()
    $('.images-wrapper').html(images)
    $('.main-trial').show()
  }

  showPausePage = () => {
    $('.main-steps').hide()
    $('.main-pause').show()
  }

  saveResult = (e) =>{
    const resp = $(e.target).data('val')
    this.results[this.index].correct = 0

    if(resp == 'no' && !imagesDetails[this.index].answer){
      this.results[this.index].correct = 1
    }

    if(resp == 'yes' && imagesDetails[this.index].answer){
      this.results[this.index].correct = 1
    }

    this.goToNextTrial()
  }

  run = () => {
    this.showPausePage()

    window.pausetimeout = setTimeout(() => {
        this.results[this.index] = {}
        this.results[this.index].startTime = new Date();

        this.renderTrial()
        window.durationtimeout = setTimeout(() => {
            this.goToNextTrial();
        }, 5000);
    }, 500);
  }
}

const focusGame = new FocusGame()

